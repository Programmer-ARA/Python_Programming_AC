# RANGE:- Range functions returns a sequnce of numbers, starting from 0 by default, and increments by 1(by defaults), and stops before a specified number.

# SYNTAX of range function.....

for i in range(10): # range(stop)
    print(i)

for i in range(2, 10): # range(start, stop)
    print(i)

for i in range(2, 10, 2): # range(start, stop, step)
    print(i)
