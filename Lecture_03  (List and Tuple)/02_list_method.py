list = [2, 1, 3]

list.append(4) # Add element at the end of the list
print(list)

list.sort() # Sort the list in ascending order
print(list)

list.sort(reverse=True) # Sort the list in descending order
print(list)

list.reverse() # reverse the list
print(list)

list.insert(3, 5) # insert element at particular element
print(list)

list.remove(2) # remove the first occurence of element  from the list
print(list)

list.pop(3) # remove the element at the index
print(list)